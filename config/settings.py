# 환경 변수 확인 (민감 정보 마스킹)
cloudinary_url = os.environ.get('CLOUDINARY_URL', '')
cloud_name = os.environ.get('CLOUDINARY_CLOUD_NAME', '')
api_key = os.environ.get('CLOUDINARY_API_KEY', '')
api_secret = os.environ.get('CLOUDINARY_API_SECRET', '')

print(f"CLOUDINARY_URL: {'SET' if cloudinary_url else '[X] NOT SET'}")
print(f"CLOUDINARY_CLOUD_NAME: {cloud_name if cloud_name else '[X] NOT SET'}")
print(f"CLOUDINARY_API_KEY: {api_key[:4] + '...' if api_key else '[X] NOT SET'}")
print(f"CLOUDINARY_API_SECRET: {'SET' if api_secret else '[X] NOT SET'}")

CLOUDINARY_STORAGE = {}

if cloudinary_url:
    print("[CLOUDINARY] Parsing CLOUDINARY_URL...")
    try:
        parsed = urlparse(cloudinary_url)
        # 1. URL 파싱 결과 우선 저장
        parsed_storage = {
            'CLOUD_NAME': parsed.hostname or '',
            'API_KEY': parsed.username or '',
            'API_SECRET': parsed.password or '',
            'SECURE': True,
        }
        print(f"[OK] Parsing success - CLOUD_NAME: {parsed.hostname}")
        
        # 2. 개별 환경 변수가 있다면 우선 적용 (Override)
        if cloud_name: parsed_storage['CLOUD_NAME'] = cloud_name
        if api_key: parsed_storage['API_KEY'] = api_key
        if api_secret: parsed_storage['API_SECRET'] = api_secret
        
        CLOUDINARY_STORAGE = parsed_storage
        
    except Exception as e:
        print(f"[X] CLOUDINARY_URL parsing failed: {e}")
        CLOUDINARY_STORAGE = {
            'CLOUD_NAME': cloud_name,
            'API_KEY': api_key,
            'API_SECRET': api_secret,
            'SECURE': True,
        }
else:
    print("[CLOUDINARY] Using individual environment variables")
    CLOUDINARY_STORAGE = {
        'CLOUD_NAME': cloud_name,
        'API_KEY': api_key,
        'API_SECRET': api_secret,
        'SECURE': True,
    }

# Cloudinary 라이브러리 직접 초기화
if CLOUDINARY_STORAGE.get('CLOUD_NAME') and CLOUDINARY_STORAGE.get('API_KEY'):
    try:
        import cloudinary
        cloudinary.config(
            cloud_name=CLOUDINARY_STORAGE['CLOUD_NAME'],
            api_key=CLOUDINARY_STORAGE['API_KEY'],
            api_secret=CLOUDINARY_STORAGE['API_SECRET'],
            secure=True
        )
        print(f"[OK] Cloudinary library initialized: {CLOUDINARY_STORAGE['CLOUD_NAME']}")
    except Exception as e:
        print(f"[X] Cloudinary library initialization failed: {e}")

# Cloudinary 활성화 여부
